public class ScreenDimensions {
    private int mWidth;
    private int mHeight;

    public ScreenDimensions(int w, int h) {
        mWidth = w;
        mHeight = h;
    }

    public int getWidth() {
        return mWidth;
    }

    public int getHeight() {
        return mHeight;
    }
}
